
package movieticketbookingsystem;

/**
 *
 * @author shad7
 */
public interface Movie {
    
     /**
     * @author shahad
     * this abstract method print type of movie
     * takes no @param
     * No @return type
     */
    public abstract void type();
    
     /**
     * @author shahad
     * this abstract method print movie names
     * takes no @param
     * No @return type
     */
    public abstract void printMovieName();
    
    /**
     * @author shahad
     * this abstract method print movie details
     * takes no @param
     * No @return type
     */
    public abstract void MovieNames();
    
    /**
     * @author farah
     * this method prints all the data of the movie 
     */
    public abstract void getMovieData();
 
}
 
   